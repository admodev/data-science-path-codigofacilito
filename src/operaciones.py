# coding: utf-8
def resta(a: int, b: int) -> int:
    """Returns the substraction of two integers"""
    return a - b
    
resultado = resta(20, 10)
print(resultado)
def suma(a: int, b: int) -> int:
    """Returns the sum of two integers"""
    return a + b
    
resultadoSuma = suma(10, 20)
print(resultadoSuma)
